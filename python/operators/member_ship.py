#membership operators
# in \\\ not in
numbers = (1, 2, 3, 4, 5)

# Check if 3 is in the tuple
print(3 in numbers)  

# Check if 10 is in the tuple
print(10 in numbers) 


#membership operators
numbers = (1, 2, 3, 4, 5)

# Check if 3 is  not in the tuple
print(3 not in numbers)

# Check if 10 is not in the tuple
print(10 not in numbers)



